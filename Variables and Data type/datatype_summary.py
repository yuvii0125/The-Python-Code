# 1. Numeric Types
# int: Integer numbers (e.g., 1, 42, -99)
# float: Floating-point numbers (e.g., 3.14, -2.5)
# complex: Complex numbers with real and imaginary parts (e.g., 3 + 4j)

#mutable means can be changed
#immutable means cannot be changed

# 2. Sequence Types
# str: Strings, used for text (e.g., "hello", 'Python')
# list: Ordered, mutable collection (e.g., [1, 2, 3])
# tuple: Ordered, immutable collection (e.g., (1, 2, 3))

# 3. Mapping Type
# dict: Collection of key-value pairs (e.g., {"name": "Alice", "age": 25})

# 4. Set Types
# set: Unordered collection of unique elements (e.g., {1, 2, 3})
# frozenset: Immutable set (e.g., frozenset({1, 2, 3}))

# 5. Boolean Type
# bool: Represents truth values (e.g., True or False)

# 6. Binary Types
# bytes: Immutable sequence of bytes (e.g., b'hello')
# bytearray: Mutable sequence of bytes
# memoryview: Provides a view of memory, useful for handling large data buffers without copying

# 7. None Type
# NoneType: Represents the absence of a value, with the single value None



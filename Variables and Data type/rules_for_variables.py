#Rules for defining variables in python

#1. A variable name must start with a letter or an underscore
#2. A variable name cannot start with a number
#3. A variable name can only contain alpha-numeric characters and underscores
#4. Variable names are case-sensitive
#5. Variable names should not be a reserved keyword
#6. space is not allowed in variable name

_hine = 10 #valid
hine = 10 #valid
hine10 = 10 #valid
hine_10 = 10 #valid
_hine_10 = 10 #valid    
